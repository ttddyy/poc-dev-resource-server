package net.ttddyy.devresourceserver.cilent;

import java.util.Map;

import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.service.annotation.GetExchange;
import org.springframework.web.service.annotation.PostExchange;

/**
 * @author Tadaya Tsuyukubo
 */
public interface DevResourceClient {

	@GetExchange("/containers/{imageName}")
	Map<String, String> get(@PathVariable String imageName);

	@PostExchange("/containers")
	Map<String, String> create(@RequestParam String imageName);

}
